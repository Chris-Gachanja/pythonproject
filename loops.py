# while loop
# incrementing values
number = 20
while number <= 25:
    print(number)
    number += 1

# Decrementing values
a = 10
while a >=1:
    print(a)
    a -=1

# For loop
languages = ["python","PHP", "Kotlin", "C++"]
for x in languages:
    print(x)

# Range
for x in range(10):
    print(x)
for b in range(15, 20):
    print(b)
for x in range(4, 30, 2):
    print(x)